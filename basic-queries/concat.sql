--The CONCAT() function adds two or more strings together.
--Syntax:
--CONCAT(string1, string2, ...., string_n)

SELECT CONCAT('SQL', ' is', ' fun!');

--Concat With +
--The + operator allows you to add two or more strings together.

--Syntax:
--string1 + string2 + string_n

SELECT 'SQL' + ' is' + ' fun!';
